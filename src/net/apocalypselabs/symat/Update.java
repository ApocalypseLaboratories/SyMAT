    private class ChangesThread extends Thread {

        @Override
        public void run() {
            final String html;
            String content;
            try {
                content = FileUtils.getUrl(Main.API_URL + "changelog.php");
            } catch (Exception ex) {
                content = "Error, cannot get changes.";
            }
            html = content;
            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    changelogBox.setText(html);
                }
            });
        }
    }


    public static void loadFrame(JInternalFrame frame) {
        mainPane.add(frame);
        frame.setLocation(
                (mainPane.getWidth() / 2) - (frame.getWidth() / 2),
                (mainPane.getHeight() / 2) - (frame.getHeight() / 2));
        frame.setVisible(true);
    }

